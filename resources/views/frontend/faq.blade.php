							<h3>WHAT IS CBD?</h3>
								<ul>
									<li style="padding-left: 30px;">CBD is an acronym for Cannabidiol (Can-a-bid-i-ol), a prominent naturally occurring class of molecules called cannabinoids found in the plant genus Cannabis Sativa L. CBD comprises up to 40% of the plant and is one of over 60 plus compounds found in cannabis. Of these compounds, CBD and THC are usually present in the highest concentrations, and are therefore the most recognized and studied. CBD is a non- psychoactive component of the plant that possesses a wide range of benefits and does not cause a high, unlike THC. Our bodies are made up of an endocannabinoid system and use cannabinoids to maintain healthy cells.  
