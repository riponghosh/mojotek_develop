							<h3>WHAT'S THE DIFFERENCE BETWEEN HEMP & MARIJUANA?</h3>
								<ul>
									<li style="padding-left: 30px;">Scientifically, industrial Hemp and Marijuana are the same plant, with a genus and species name of Cannabis Sativa. They have a drastically different genetic profile though. Industrial Hemp is always a strain of Cannabis sativa, while marijuana can be Cannabis sativa, Cannabis indica, or Cannabis ruderalis. The major difference is how industrial hemp has been bred compared to a marijuana form of Cannabis sativa. Typically speaking, industrial hemp is very fibrous, with long strong stalks, and barely has any flowering buds, while a marijuana strain of Cannabis sativa will be smaller, bushier, and full of flowering buds. However, newer industrial hemp varieties in the USA are being bred to have more flowers and higher yields of cannabinoids and terpenes, such as our Kentucky hemp we’re now using!<br>
									99% of the time marijuana has a high amount of THC and only a very low amount of CBD. Hemp, on the other hand, naturally has a very high amount of CBD in most instances, and only a trace amount of THC. Fortunately, the cannabinoid profile of hemp is ideal for people looking for benefits from cannabis without the ‘high.’ Hemp is used for making herbal supplements, food, fiber, rope, paper, bricks, oil, natural plastic, and so much more, whereas marijuana is usually used just recreationally, spiritually, and medicinally. The term cannabis oil can refer to either a marijuana or hemp derived oil, since marijuana and hemp are two different forms of cannabis.<br>
									In the USA the legal definition of “industrial hemp,” per Section 7606 of the Agricultural Appropriations Act of 2014, is “INDUSTRIAL HEMP — The term ‘‘industrial hemp’’ means the plant Cannabis sativa L. and any part of such plant, whether growing or not, with a delta-9 tetrahydrocannabinol concentration of not more than 0.3 percent on a dry weight basis.”
 
									</li>
								</ul>
